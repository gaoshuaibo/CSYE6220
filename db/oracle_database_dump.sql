  CREATE OR REPLACE PROCEDURE "ORACLE_USER1"."MULTIPLYSALARYBY" (num in number) is 
CURSOR sal_cur is SELECT empno, salary FROM employee; 
r_emp sal_cur%ROWTYPE;   
avg_sal number(7,2); 
new_salary number; 
neg_mult EXCEPTION;
BEGIN 
IF num < 0 THEN
RAISE neg_mult;
END IF;
SELECT avg(salary) INTO avg_sal FROM employee;
FOR emp_rec in sal_cur
  LOOP
    FETCH sal_cur into r_emp;
    exit when sal_cur%NOTFOUND; 
    IF emp_rec.Salary < avg_sal THEN
      new_salary := emp_rec.Salary * num;
    ELSE       
      new_salary := emp_rec.Salary * 1.5; 
    END IF;
  END LOOP; 
  CLOSE sal_cur; 
  UPDATE employee SET salary = new_salary; 
EXCEPTION
   WHEN neg_mult THEN
    dbms_output.put_line('multiplier must be greater than zero!'); 
   WHEN others THEN
dbms_output.put_line('Error!');
END;

